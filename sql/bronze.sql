create table public.cbx_comunicados (
  id smallint generated by default as identity not null,
  titulo text null,
  link text null,
  data_hora text null,
  created_at timestamp with time zone not null default now(),
  constraint cbx_comunicados_pkey primary key (id),
  constraint cbx_comunicados_cbx_link_key unique (link)
) TABLESPACE pg_default;


