  fide_id text null,
  nome text null,
  nascimento text null,
  uf text null,
  classico text null,
  rapido text null,
  blitz text null,
  created_at timestamp with time zone not null default now(),
  constraint cbx_jogadores_pkey primary key (id),
  constraint cbx_jogadores_cbx_id_key unique (cbx_id)
